class TripResult {
  const TripResult({
    required this.from,
    required this.to,
    required this.departLabel,
    required this.durationLabel,
    required this.priceLabel,
    required this.statusLabel,
  });

  final String from;
  final String to;
  final String departLabel;
  final String durationLabel;
  final String priceLabel;
  final String statusLabel;
}
